"""
zuspec.synth: Synthesis transformations for Zuspec IR.

This package provides:
- SPRTL (Synchronous Process RTL) transformations
- FSM generation from async processes
- Scheduling algorithms (ASAP, ALAP, list-based)
- Code generation backends
"""

from . import sprtl


def synthesize(cls, *, output=None, top=None, reset_style="sync_low",
               sdc_output=None, forward=True) -> str:
    """Synthesize a Zuspec component class to SystemVerilog.

    Detects whether *cls* is a pipeline component (annotated with
    ``@zdc.pipeline``) or a sequential/combinational component and routes
    to the appropriate internal pass chain.

    Parameters
    ----------
    cls:
        Component class decorated with ``@zdc.dataclass``.
    output:
        File path to write the ``.sv`` file.  If omitted the SV text is
        returned but not written.
    top:
        Override the generated module name (default: ``cls.__name__``).
    reset_style:
        ``"sync_low"`` (default), ``"sync_high"``, ``"async_low"``,
        ``"async_high"``.  Ignored when the component declares an explicit
        ``ResetDomain`` (the domain's ``style`` and ``polarity`` take
        precedence).
    sdc_output:
        File path to write a Synopsys Design Constraints (``.sdc``) file.
        Only generated when the component declares a ``ClockDomain`` with a
        known ``period``.
    forward:
        Enable RAW hazard forwarding for pipeline components.  Ignored for
        non-pipeline components.

    Returns
    -------
    str
        Generated SystemVerilog source code.
    """
    is_pipeline = _detect_pipeline(cls)
    if is_pipeline:
        sv = _synthesize_pipeline(cls, forward=forward, top=top)
    else:
        sv = _synthesize_sprtl(cls, reset_style=reset_style, top=top)

    if output is not None and sv:
        with open(output, "w") as fh:
            fh.write(sv)

    if sdc_output is not None:
        sdc = _generate_sdc(cls, top=top)
        if sdc:
            with open(sdc_output, "w") as fh:
                fh.write(sdc)

    return sv


def _generate_sdc(cls, top=None) -> str:
    """Generate a minimal SDC file from a component's ClockDomain declarations.

    Returns an empty string if the component has no ``ClockDomain`` with a
    known ``period``.

    When the component is (or contains) a :class:`~zuspec.dataclasses.cdc.TwoFFSync`,
    a ``set_false_path`` constraint is also emitted for the first synchroniser
    stage register.
    """
    from zuspec.dataclasses.data_model_factory import DataModelFactory
    from zuspec.ir.core.data_type import DataTypeComponent
    from zuspec.synth.sprtl.fsm_ir import DomainBinding

    ctx = DataModelFactory().build(cls)
    type_name = getattr(cls, "__qualname__", None) or cls.__name__
    component_ir = ctx.type_m.get(type_name) or ctx.type_m.get(cls.__name__)
    if component_ir is None:
        return ""

    db = DomainBinding.from_component_ir(component_ir)
    module_name = top or cls.__name__

    lines = [f"# Generated by zuspec-synth for {module_name}"]

    # create_clock only when period is known
    if db.period is not None:
        try:
            period_ns = db.period.as_ns()
        except Exception:
            period_ns = float(str(db.period))
        lines.append(
            f"create_clock -name {db.clock_name} -period {period_ns}"
            f" [get_ports {db.clock_name}]"
        )

    # ------------------------------------------------------------------ #
    # TwoFFSync set_false_path generation                                  #
    # ------------------------------------------------------------------ #
    # Case 1: the class itself IS a TwoFFSync primitive.
    if getattr(cls, "_zdc_two_ff_sync", False):
        src_clk = getattr(cls, "src_domain", None) or "*"
        lines.append(
            f"set_false_path -from [get_clocks {{{src_clk}}}]"
            f" -to [get_cells {{*_sync0_reg}}]"
        )
    else:
        # Case 2: look for TwoFFSync sub-instances in the hierarchy.
        import dataclasses as _dc
        py_fields_meta: dict = {}
        if hasattr(cls, "__dataclass_fields__"):
            for pf in _dc.fields(cls):
                py_fields_meta[pf.name] = (pf.metadata, pf.type)

        for f in component_ir.fields:
            if type(f).__name__ != "Field":
                continue
            _, py_type = py_fields_meta.get(f.name, ({}, None))
            py_cls = py_type if isinstance(py_type, type) else None
            if py_cls and getattr(py_cls, "_zdc_two_ff_sync", False):
                src_clk = getattr(py_cls, "src_domain", None) or "*"
                lines.append(
                    f"set_false_path -from [get_clocks {{{src_clk}}}]"
                    f" -to [get_cells {{*{f.name}/_sync0_reg}}]"
                )

    result = "\n".join(lines) + "\n"
    # If the only line is the header comment, return empty (nothing useful).
    if len(lines) == 1:
        return ""
    return result

