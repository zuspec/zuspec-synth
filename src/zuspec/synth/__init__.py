def _detect_pipeline(cls) -> bool:
    """Return True if *cls* has a method decorated with ``@zdc.pipeline``."""
    for name in dir(cls):
        try:
            member = getattr(cls, name)
        except AttributeError:
            continue
        if callable(member) and hasattr(member, "_zdc_pipeline"):
            return True
    return False


def _reset_style_enum(reset_style: str):
    from zuspec.synth.sprtl import ResetStyle
    _map = {
        "sync_low":   ResetStyle.SYNC_LOW,
        "sync_high":  ResetStyle.SYNC_HIGH,
        "async_low":  ResetStyle.ASYNC_LOW,
        "async_high": ResetStyle.ASYNC_HIGH,
    }
    return _map.get(reset_style.lower(), ResetStyle.SYNC_LOW)


def _synthesize_pipeline(cls, *, forward: bool = True, top=None) -> str:
    """Run the pipeline pass chain and return SV text."""
    from zuspec.dataclasses.data_model_factory import DataModelFactory
    from zuspec.synth.ir.synth_ir import SynthIR, SynthConfig
    from zuspec.synth.passes import (
        PipelineFrontendPass,
        AutoThreadPass,
        HazardAnalysisPass,
        ForwardingGenPass,
        StallGenPass,
        SyncBodyLowerPass,
        SVEmitPass,
    )

    cfg = SynthConfig(forward_default=True if forward else False)

    ir = SynthIR()
    ir.component = cls
    ir.model_context = DataModelFactory().build(cls)

    for pass_cls in [
        PipelineFrontendPass,
        AutoThreadPass,
        HazardAnalysisPass,
        ForwardingGenPass,
        StallGenPass,
        SyncBodyLowerPass,
    ]:
        ir = pass_cls(cfg).run(ir)

    ir = SVEmitPass(cfg).run(ir)
    sv = ir.lowered_sv.get("sv/pipeline/top", "")

    if top and sv:
        # Replace the first occurrence of the default module name
        sv = sv.replace(f"module {cls.__name__}", f"module {top}", 1)

    return sv


def _synthesize_sprtl(cls, *, reset_style: str = "sync_low", top=None) -> str:
    """Run the SPRTL transformer + SV codegen and return SV text.

    Handles both sync/comb mixed components (via FSM) and pure-comb
    components (via a simple always_comb module skeleton).
    Handles hierarchical components (sub-instances) via _synthesize_hierarchical.
    """
    from zuspec.dataclasses.data_model_factory import DataModelFactory
    from zuspec.synth.sprtl import (
        SPRTLTransformer,
        generate_sv,
        SVGenConfig,
    )

    ctx = DataModelFactory().build(cls)
    module_name = top or cls.__name__

    # DataModelFactory keys type_m by __qualname__ when available
    component_ir = ctx.type_m.get(getattr(cls, "__qualname__", None)) \
                   or ctx.type_m.get(cls.__name__)

    if component_ir is None:
        raise ValueError(
            f"synthesize: could not find IR for {cls.__name__!r}.  "
            f"Available keys: {list(ctx.type_m.keys())}"
        )

    # Hierarchical components (with sub-instances) get their own path.
    if _is_hierarchical(component_ir, ctx):
        return _synthesize_hierarchical(cls, component_ir, ctx, module_name)

    sync_processes = getattr(component_ir, "sync_processes", [])
    proc_processes = getattr(component_ir, "proc_processes", [])

    if sync_processes:
        proc = sync_processes[0]
        if getattr(proc, "is_async", False):
            # Async (while True / await) path — use the SPRTL FSM transformer.
            reset_cfg = SVGenConfig(reset_style=_reset_style_enum(reset_style))
            transformer = SPRTLTransformer()
            fsm = transformer.transform(component_ir, proc)
            fsm.name = module_name
            sv = generate_sv(fsm, config=reset_cfg)
        else:
            # Plain-def one-cycle sync path — emit always_ff directly from IR.
            sv = _synthesize_simple_sync(
                component_ir, proc, module_name, reset_style,
                ctx=ctx, py_cls=cls,
            )
    elif proc_processes:
        # @zdc.proc Process nodes (e.g. Counter pattern)
        reg_proc_nodes = [p for p in proc_processes if type(p).__name__ == 'Process']
        if reg_proc_nodes:
            sv = _synthesize_reg_process(component_ir, reg_proc_nodes[0], module_name)
        else:
            sv = _build_comb_module(component_ir, module_name)
    else:
        # Pure-comb path: build a simple always_comb module skeleton.
        sv = _build_comb_module(component_ir, module_name)

    return sv


