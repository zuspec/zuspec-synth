def _is_hierarchical(component_ir, ctx) -> bool:
    """Return True if the component has sub-component instance fields."""
    for f in component_ir.fields:
        if type(f).__name__ == "Field":
            dt = getattr(f, "datatype", None)
            if dt and type(dt).__name__ == "DataTypeRef":
                ref_ir = ctx.type_m.get(dt.ref_name)
                if ref_ir and type(ref_ir).__name__ == "DataTypeComponent":
                    return True
    return False


def _decode_bind_ref_h(expr, top_fields, ctx):
    """Decode one side of a bind_map entry.

    Returns ``(inst_name, port_name, field_ir, is_bundle, struct_ir)``:
    - ``inst_name``: sub-instance field name, or ``None`` for a top-level ref.
    - ``port_name``: port/field name within the inst (or top-level name).
    - ``field_ir``: the IR field object.
    - ``is_bundle``: True when the field type is a struct (bundle/mirror).
    - ``struct_ir``: the struct IR if is_bundle, else None.
    """
    t = type(expr).__name__
    if t != "ExprRefField":
        return (None, "?", None, False, None)

    base_t = type(expr.base).__name__
    if base_t == "TypeExprRefSelf":
        # Direct top-level field reference.
        f = top_fields[expr.index]
        return (None, f.name, f, False, None)

    if base_t == "ExprRefField":
        # Sub-instance field reference: expr.base is the inst field.
        inst_f = top_fields[expr.base.index]
        inst_name = inst_f.name
        dt = getattr(inst_f, "datatype", None)
        if dt and type(dt).__name__ == "DataTypeRef":
            sub_ir = ctx.type_m.get(dt.ref_name)
            if sub_ir:
                sub_fields = list(sub_ir.fields)
                sf = sub_fields[expr.index]
                sf_dt = getattr(sf, "datatype", None)
                is_bundle = False
                struct_ir = None
                if sf_dt and type(sf_dt).__name__ == "DataTypeRef":
                    cand = ctx.type_m.get(sf_dt.ref_name)
                    if cand and type(cand).__name__ == "DataTypeStruct":
                        is_bundle = True
                        struct_ir = cand
                return (inst_name, sf.name, sf, is_bundle, struct_ir)

    return (None, "?", None, False, None)


def _synthesize_hierarchical(cls, component_ir, ctx, module_name: str) -> str:
    """Generate a hierarchical wrapper module for a component with sub-instances."""
    import dataclasses as _dc

    top_fields = list(component_ir.fields)

    # Python class metadata: bundle/mirror/instance kind per field.
    py_fields_meta = {}
    if hasattr(cls, "__dataclass_fields__"):
        for pf in _dc.fields(cls):
            py_fields_meta[pf.name] = (pf.metadata, pf.type)

    # ------------------------------------------------------------------ #
    # 1. Classify top-level fields                                         #
    # ------------------------------------------------------------------ #
    top_scalar_ports = []  # (name, bits, is_out)
    sub_insts = []         # (field_name, type_name, sub_ir, sub_py_cls)

    for f in top_fields:
        ft = type(f).__name__
        if ft == "FieldInOut":
            bits = _field_bits(f)
            is_out = getattr(f, "is_out", False)
            top_scalar_ports.append((f.name, bits, is_out))
        elif ft == "Field":
            dt = getattr(f, "datatype", None)
            if dt and type(dt).__name__ == "DataTypeRef":
                ref_ir = ctx.type_m.get(dt.ref_name)
                if ref_ir and type(ref_ir).__name__ == "DataTypeComponent":
                    meta, py_type = py_fields_meta.get(f.name, ({}, None))
                    sub_py_cls = py_type if isinstance(py_type, type) else None
                    sub_insts.append((f.name, dt.ref_name, ref_ir, sub_py_cls))

    # ------------------------------------------------------------------ #
    # 2. Parse bind_map                                                    #
    # ------------------------------------------------------------------ #
    bound_ports = set()          # (inst_name, expanded_port_name)
    inst_port_to_top = {}        # (inst_name, port_name) → top_port_name
    internal_wires = {}          # wire_name → bits
    # bundle_wire_groups: info for connecting bundle-to-bundle binds
    # list of (wire_base, struct_ir, lhs_inst, lhs_port, rhs_inst, rhs_port)
    bundle_wire_groups = []

    for bind in component_ir.bind_map:
        lhs_info = _decode_bind_ref_h(bind.lhs, top_fields, ctx)
        rhs_info = _decode_bind_ref_h(bind.rhs, top_fields, ctx)
        lhs_inst, lhs_port, _lf, lhs_is_bundle, lhs_struct = lhs_info
        rhs_inst, rhs_port, _rf, rhs_is_bundle, rhs_struct = rhs_info

        if lhs_is_bundle or rhs_is_bundle:
            # Bundle-to-bundle: create internal wires for each struct field.
            struct_ir = lhs_struct or rhs_struct
            wire_base = lhs_port  # e.g. "io"
            if struct_ir:
                bundle_wire_groups.append(
                    (wire_base, struct_ir, lhs_inst, lhs_port, rhs_inst, rhs_port)
                )
                for sf in struct_ir.fields:
                    wire_name = f"{wire_base}_{sf.name}"
                    internal_wires[wire_name] = _field_bits(sf)
                    if lhs_inst:
                        bound_ports.add((lhs_inst, f"{lhs_port}_{sf.name}"))
                    if rhs_inst:
                        bound_ports.add((rhs_inst, f"{rhs_port}_{sf.name}"))
        elif lhs_inst and not rhs_inst:
            # inst.port ← top.port
            bound_ports.add((lhs_inst, lhs_port))
            inst_port_to_top[(lhs_inst, lhs_port)] = rhs_port
        elif rhs_inst and not lhs_inst:
            bound_ports.add((rhs_inst, rhs_port))
            inst_port_to_top[(rhs_inst, rhs_port)] = lhs_port

    # ------------------------------------------------------------------ #
    # 3. Collect unbound sub-instance ports → expose at top level          #
    # ------------------------------------------------------------------ #
    exposed_ports = []  # (port_name, bits, is_out)
    # Track which names we've already exposed (de-dup)
    exposed_names = set()

    for inst_name, _type_name, sub_ir, sub_py_cls in sub_insts:
        sub_py_meta = {}
        if sub_py_cls and hasattr(sub_py_cls, "__dataclass_fields__"):
            for pf in _dc.fields(sub_py_cls):
                sub_py_meta[pf.name] = pf.metadata

        for sf in sub_ir.fields:
            sft = type(sf).__name__
            if sft == "FieldInOut":
                pname = sf.name
                if (inst_name, pname) not in bound_ports and pname not in exposed_names:
                    bits = _field_bits(sf)
                    is_out = getattr(sf, "is_out", False)
                    exposed_ports.append((pname, bits, is_out))
                    exposed_names.add(pname)
            elif sft == "Field":
                sf_dt = getattr(sf, "datatype", None)
                if sf_dt and type(sf_dt).__name__ == "DataTypeRef":
                    struct_ir = ctx.type_m.get(sf_dt.ref_name)
                    if struct_ir and type(struct_ir).__name__ == "DataTypeStruct":
                        is_mirror = sub_py_meta.get(sf.name, {}).get("kind") == "mirror"
                        for ssf in struct_ir.fields:
                            expanded = f"{sf.name}_{ssf.name}"
                            if (inst_name, expanded) not in bound_ports and expanded not in exposed_names:
                                ssf_is_out = getattr(ssf, "is_out", True)
                                if is_mirror:
                                    ssf_is_out = not ssf_is_out
                                exposed_ports.append((expanded, _field_bits(ssf), ssf_is_out))
                                exposed_names.add(expanded)

    # ------------------------------------------------------------------ #
    # 4. Synthesize sub-components first, then emit the wrapper module.    #
    # ------------------------------------------------------------------ #
    sub_sv_parts = []
    for _inst_name, type_name, sub_ir, sub_py_cls in sub_insts:
        if sub_py_cls is not None:
            sub_sv = _synthesize_sprtl(sub_py_cls, reset_style="sync_low")
            if sub_sv:
                # Strip the common file header so timescale/comment appear once.
                stripped = sub_sv
                for hdr in ("// Generated by zuspec-synth\n", "`timescale 1ns/1ps\n"):
                    stripped = stripped.replace(hdr, "", 1)
                sub_sv_parts.append(stripped.lstrip("\n"))

    all_top_ports = list(top_scalar_ports) + exposed_ports
    port_decls = []
    for pname, bits, is_out in all_top_ports:
        dir_kw = "output" if is_out else "input "
        width_str = _width_str(bits)
        port_decls.append(f"  {dir_kw} logic {width_str}{pname}")

    wrapper_lines = [
        f"module {module_name} (",
        ",\n".join(port_decls),
        ");",
        "",
    ]

    # Internal wire declarations for bundle connections.
    if internal_wires:
        for wname, wbits in internal_wires.items():
            wrapper_lines.append(f"  logic {_width_str(wbits)}{wname};")
        wrapper_lines.append("")

    # Sub-instance instantiations.
    for inst_name, type_name, sub_ir, sub_py_cls in sub_insts:
        sub_py_meta = {}
        if sub_py_cls and hasattr(sub_py_cls, "__dataclass_fields__"):
            for pf in _dc.fields(sub_py_cls):
                sub_py_meta[pf.name] = pf.metadata

        port_conns = []
        for sf in sub_ir.fields:
            sft = type(sf).__name__
            if sft == "FieldInOut":
                pname = sf.name
                if (inst_name, pname) in inst_port_to_top:
                    conn = inst_port_to_top[(inst_name, pname)]
                elif pname in exposed_names:
                    conn = pname  # direct pass-through
                else:
                    conn = pname  # fallback
                port_conns.append(f"    .{pname}({conn})")
            elif sft == "Field":
                sf_dt = getattr(sf, "datatype", None)
                if sf_dt and type(sf_dt).__name__ == "DataTypeRef":
                    struct_ir = ctx.type_m.get(sf_dt.ref_name)
                    if struct_ir and type(struct_ir).__name__ == "DataTypeStruct":
                        for ssf in struct_ir.fields:
                            wire_name = f"{sf.name}_{ssf.name}"
                            port_conns.append(f"    .{sf.name}_{ssf.name}({wire_name})")

        wrapper_lines.append(f"  {type_name} {inst_name}_inst (")
        wrapper_lines.append(",\n".join(port_conns))
        wrapper_lines.append("  );")
        wrapper_lines.append("")

    wrapper_lines.append("endmodule")

    # Combine: file header + sub-component SV + wrapper module.
    header = "// Generated by zuspec-synth\n`timescale 1ns/1ps\n"
    return header + "\n".join(sub_sv_parts) + "\n" + "\n".join(wrapper_lines) + "\n"


def _synthesize_reg_process(component_ir, process_ir, module_name: str) -> str:
    """Emit an always @(posedge clock or posedge reset) module for a @zdc.proc register process.

    Handles the 'while True: await reg.write(...)' pattern. Uses Verilog-2005
    style (reg, always, <=) for Yosys compatibility.
    """
    fields = list(component_ir.fields)
    idx_to_name = {i: f.name for i, f in enumerate(fields)}

    # Collect Reg output fields
    reg_fields = [f for f in fields if getattr(f, 'is_reg', False)]

    # --- Port list ---
    port_lines = ["    input clock", "    input reset"]
    for f in reg_fields:
        bits = _field_bits(f)
        w = f"[{bits-1}:0] " if bits > 1 else ""
        port_lines.append(f"    output reg {w}{f.name}")

    ports = ",\n".join(port_lines)

    lines = [f"module {module_name}(", ports + ");"]
    lines.append("")

    # --- always block ---
    lines.append("  always @(posedge clock or posedge reset) begin")
    lines.append("    if (reset) begin")
    for f in reg_fields:
        rv = f.reset_value if f.reset_value is not None else 0
        lines.append(f"      {f.name} <= {rv};")
    lines.append("    end else begin")

    # Lower the process body — unwrap outer StmtWhile if present
    body_stmts = process_ir.body
    if body_stmts and type(body_stmts[0]).__name__ == "StmtWhile":
        body_stmts = body_stmts[0].body

    body_lines = _ir_stmts_to_sv(body_stmts, idx_to_name, indent=6)
    lines.extend(body_lines)

    lines.append("    end")
    lines.append("  end")
    lines.append("")
    lines.append("endmodule")
    return "\n".join(lines)


def _build_comb_module(component_ir, module_name: str) -> str:
    """Generate a minimal always_comb SV module for a pure-comb component."""
    lines: list[str] = []
    fields = getattr(component_ir, "fields", [])

    # Collect port declarations using is_out (direction is None in DC IR)
    ports: list[str] = []
    output_names: list[str] = []
    # idx_to_name uses enumeration index (same as _synthesize_simple_sync)
    idx_to_name: dict = {i: f.name for i, f in enumerate(fields)}
    for f in fields:
        is_out = getattr(f, "is_out", None)
        if is_out is None:
            continue
        dir_kw = "output" if is_out else "input "
        if is_out:
            output_names.append(f.name)
        bits = _field_bits(f)
        width_str = _width_str(bits)
        ports.append(f"  {dir_kw} logic {width_str}{f.name}")

    port_block = (",\n").join(ports)
    lines.append(f"module {module_name}(")
    lines.append(port_block)
    lines.append(");")
    lines.append("")

    comb_processes = getattr(component_ir, "comb_processes", [])
    for comb_proc in comb_processes:
        lines.append("  always_comb begin")
        # Default-to-zero for all outputs (prevents latches)
        for name in output_names:
            lines.append(f"    {name} = '0;")
        lines.extend(_ir_stmts_to_sv_comb(getattr(comb_proc, "body", []), idx_to_name, 4))
        lines.append("  end")
        lines.append("")

    if not comb_processes:
        lines.append("  always_comb begin")
        for name in output_names:
            lines.append(f"    {name} = '0;")
        lines.append("  end")
        lines.append("")

    lines.append("endmodule")

    return "\n".join(lines) + "\n"


